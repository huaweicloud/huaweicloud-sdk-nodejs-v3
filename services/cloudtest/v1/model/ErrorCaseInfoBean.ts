

export class ErrorCaseInfoBean {
    private 'error_code'?: string;
    private 'error_msg'?: string;
    private 'testcase_id'?: string;
    public constructor() { 
    }
    public withErrorCode(errorCode: string): ErrorCaseInfoBean {
        this['error_code'] = errorCode;
        return this;
    }
    public set errorCode(errorCode: string  | undefined) {
        this['error_code'] = errorCode;
    }
    public get errorCode(): string | undefined {
        return this['error_code'];
    }
    public withErrorMsg(errorMsg: string): ErrorCaseInfoBean {
        this['error_msg'] = errorMsg;
        return this;
    }
    public set errorMsg(errorMsg: string  | undefined) {
        this['error_msg'] = errorMsg;
    }
    public get errorMsg(): string | undefined {
        return this['error_msg'];
    }
    public withTestcaseId(testcaseId: string): ErrorCaseInfoBean {
        this['testcase_id'] = testcaseId;
        return this;
    }
    public set testcaseId(testcaseId: string  | undefined) {
        this['testcase_id'] = testcaseId;
    }
    public get testcaseId(): string | undefined {
        return this['testcase_id'];
    }
}